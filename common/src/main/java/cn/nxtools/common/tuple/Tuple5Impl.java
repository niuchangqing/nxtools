package cn.nxtools.common.tuple;

class Tuple5Impl<T1, T2, T3, T4, T5> extends Tuple5<T1, T2, T3, T4, T5> {

    public Tuple5Impl(T1 first, T2 second, T3 third, T4 fourth, T5 fifth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
        this.fifth = fifth;
    }
}
